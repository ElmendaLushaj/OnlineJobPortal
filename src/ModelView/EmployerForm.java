    private void tabelaSelectedIndexChange(){
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(e.getValueIsAdjusting()){
                    return;
                }
                
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if(selectedIndex > -1){
                    Employer em = etm.getEmployer(selectedIndex);
                   // idField.setText(em.getEmployerID().toString());
                    nameField.setText(em.getEName());
                   // String cont = new Integer(em.getEContact());
                    //contactField.setText(em.getEContact().toString());
                    contactField.setText(Integer.toString(em.getEContact()));
                    emailField.setText(em.getEEmail());
                }
            }
        });
    }

